"""Analyze correlation between eRisk depression labels and behavioral pipeline risk zones.

After running:
  1. erisk_to_spendlogs.py (insert eRisk data into SpendLogs)
  2. Behavioral aggregator (run for all erisk_ users)

This script compares ground truth labels (0=control, 1=depression)
with computed risk zones (GREEN/YELLOW/RED) to measure detection accuracy.

Usage:
    py experiments/erisk_correlation.py
    py experiments/erisk_correlation.py --output results/erisk_results.csv
"""

import argparse
import asyncio
import json
import logging
import sys
from collections import Counter
from pathlib import Path

logging.basicConfig(level=logging.INFO, format="%(levelname)s %(message)s")
logger = logging.getLogger(__name__)

ERISK_USER_PREFIX = "erisk_"


async def fetch_all_erisk_profiles() -> list[dict]:
    """Fetch UserBehaviorProfile for all erisk_ users."""
    sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "src"))

    from database import Session
    from behavioral.models import UserBehaviorProfile
    from sqlalchemy import select

    async with Session() as session:
        query = select(UserBehaviorProfile).where(
            UserBehaviorProfile.end_user_id.like(f"{ERISK_USER_PREFIX}%")
        )
        result = await session.execute(query)
        profiles = result.scalars().all()

    return [
        {
            "end_user_id": p.end_user_id,
            "risk_zone": p.risk_zone,
        }
        for p in profiles
    ]


async def fetch_metrics_history(end_user_id: str) -> list[dict]:
    """Fetch all MetricsHistory rows for a user."""
    sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "src"))

    from database import Session
    from behavioral.models import MetricsHistory
    from sqlalchemy import select

    async with Session() as session:
        query = (
            select(MetricsHistory)
            .where(MetricsHistory.end_user_id == end_user_id)
            .order_by(MetricsHistory.computed_at.asc())
        )
        result = await session.execute(query)
        rows = result.scalars().all()

    return [
        {
            "date": r.computed_at.date().isoformat() if r.computed_at else None,
            "risk_zone": r.risk_zone,
            "behavioral_scores": r.behavioral_scores or {},
            "danger_class_agg": r.danger_class_agg or {},
            "temporal_metrics": r.temporal_metrics or {},
        }
        for r in rows
    ]


async def fetch_events(end_user_id: str) -> list[dict]:
    """Fetch BehavioralEvents for a user."""
    sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "src"))

    from database import Session
    from behavioral.models import BehavioralEvent
    from sqlalchemy import select

    async with Session() as session:
        query = (
            select(BehavioralEvent)
            .where(BehavioralEvent.end_user_id == end_user_id)
            .order_by(BehavioralEvent.detected_at.asc())
        )
        result = await session.execute(query)
        rows = result.scalars().all()

    return [
        {
            "date": r.detected_at.date().isoformat() if r.detected_at else None,
            "event_type": r.event_type,
            "details": r.details or {},
        }
        for r in rows
    ]


def load_labels() -> dict[str, int]:
    """Load erisk_labels.json generated by erisk_to_spendlogs.py."""
    labels_path = Path(__file__).parent / "erisk_labels.json"
    if not labels_path.exists():
        logger.error("erisk_labels.json not found. Run erisk_to_spendlogs.py first (without --dry-run).")
        return {}
    with labels_path.open() as f:
        return json.load(f)


def zone_to_numeric(zone: str) -> int:
    return {"GREEN": 0, "YELLOW": 1, "RED": 2}.get(zone, 0)


def compute_max_zone(history: list[dict]) -> str:
    """Get the highest risk zone ever reached."""
    zones = [h["risk_zone"] for h in history if h.get("risk_zone")]
    if not zones:
        return "GREEN"
    return max(zones, key=lambda z: zone_to_numeric(z))


def compute_yellow_red_days(history: list[dict]) -> dict:
    """Count days in each zone."""
    counter = Counter(h["risk_zone"] for h in history if h.get("risk_zone"))
    return {
        "green_days": counter.get("GREEN", 0),
        "yellow_days": counter.get("YELLOW", 0),
        "red_days": counter.get("RED", 0),
        "total_days": sum(counter.values()),
    }


async def analyze():
    parser = argparse.ArgumentParser(description="eRisk correlation analysis")
    parser.add_argument("--output", type=Path, default=None, help="CSV output path")
    parser.add_argument("--detailed", action="store_true", help="Print per-user details")
    args = parser.parse_args()

    labels = load_labels()
    if not labels:
        return

    profiles = await fetch_all_erisk_profiles()
    profile_map = {p["end_user_id"]: p for p in profiles}

    logger.info("Labels: %d users, Profiles: %d users", len(labels), len(profiles))

    # Collect per-user results
    results = []
    for user_id, label in sorted(labels.items()):
        profile = profile_map.get(user_id)
        current_zone = profile["risk_zone"] if profile else "NO_DATA"

        history = await fetch_metrics_history(user_id) if profile else []
        max_zone = compute_max_zone(history)
        zone_days = compute_yellow_red_days(history)
        events = await fetch_events(user_id) if profile else []

        results.append({
            "user_id": user_id,
            "label": label,
            "label_str": "depression" if label == 1 else "control",
            "current_zone": current_zone,
            "max_zone": max_zone,
            "green_days": zone_days["green_days"],
            "yellow_days": zone_days["yellow_days"],
            "red_days": zone_days["red_days"],
            "total_days": zone_days["total_days"],
            "zone_changes": len(events),
        })

    # Compute metrics
    depression_users = [r for r in results if r["label"] == 1]
    control_users = [r for r in results if r["label"] == 0]

    depression_flagged = [r for r in depression_users if r["max_zone"] in ("YELLOW", "RED")]
    control_flagged = [r for r in control_users if r["max_zone"] in ("YELLOW", "RED")]

    depression_red = [r for r in depression_users if r["max_zone"] == "RED"]
    control_red = [r for r in control_users if r["max_zone"] == "RED"]

    print("\n" + "=" * 70)
    print("eRisk T2 x Behavioral Pipeline Correlation Analysis")
    print("=" * 70)

    print(f"\nDataset: {len(results)} users ({len(depression_users)} depression, {len(control_users)} control)")
    print(f"Users with pipeline data: {sum(1 for r in results if r['current_zone'] != 'NO_DATA')}")

    print(f"\n--- Detection Rates ---")
    if depression_users:
        print(f"Depression users flagged (YELLOW/RED): {len(depression_flagged)}/{len(depression_users)} ({len(depression_flagged)/len(depression_users)*100:.1f}%)")
        print(f"Depression users at RED:                {len(depression_red)}/{len(depression_users)} ({len(depression_red)/len(depression_users)*100:.1f}%)")
    if control_users:
        print(f"Control users flagged (YELLOW/RED):     {len(control_flagged)}/{len(control_users)} ({len(control_flagged)/len(control_users)*100:.1f}%)")
        print(f"Control users at RED:                   {len(control_red)}/{len(control_users)} ({len(control_red)/len(control_users)*100:.1f}%)")

    # Confusion matrix (YELLOW/RED = positive prediction)
    tp = len(depression_flagged)
    fn = len(depression_users) - tp
    fp = len(control_flagged)
    tn = len(control_users) - fp

    print(f"\n--- Confusion Matrix (flagged = YELLOW or RED) ---")
    print(f"                  Predicted Positive  Predicted Negative")
    print(f"  Actually Pos    TP = {tp:<16d} FN = {fn}")
    print(f"  Actually Neg    FP = {fp:<16d} TN = {tn}")

    if tp + fp > 0:
        precision = tp / (tp + fp)
        print(f"\n  Precision: {precision:.3f}")
    if tp + fn > 0:
        recall = tp / (tp + fn)
        sensitivity = recall
        print(f"  Recall (Sensitivity): {recall:.3f}")
    if tn + fp > 0:
        specificity = tn / (tn + fp)
        print(f"  Specificity: {specificity:.3f}")
    if tp + fp > 0 and tp + fn > 0:
        f1 = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0
        print(f"  F1 Score: {f1:.3f}")

    # Average zone days
    print(f"\n--- Average Days per Zone ---")
    if depression_users:
        avg_yellow = sum(r["yellow_days"] for r in depression_users) / len(depression_users)
        avg_red = sum(r["red_days"] for r in depression_users) / len(depression_users)
        print(f"  Depression: {avg_yellow:.1f} YELLOW days, {avg_red:.1f} RED days avg")
    if control_users:
        avg_yellow = sum(r["yellow_days"] for r in control_users) / len(control_users)
        avg_red = sum(r["red_days"] for r in control_users) / len(control_users)
        print(f"  Control:    {avg_yellow:.1f} YELLOW days, {avg_red:.1f} RED days avg")

    # Detailed per-user output
    if args.detailed:
        print(f"\n--- Per-User Details ---")
        for r in sorted(results, key=lambda x: (-x["label"], -zone_to_numeric(x["max_zone"]))):
            flag = "!!" if r["label"] == 1 and r["max_zone"] == "GREEN" else "  "
            print(f"  {flag} {r['user_id']:<30s} label={r['label_str']:<12s} "
                  f"max={r['max_zone']:<7s} G={r['green_days']} Y={r['yellow_days']} R={r['red_days']} "
                  f"events={r['zone_changes']}")

    # CSV export
    if args.output:
        import csv
        args.output.parent.mkdir(parents=True, exist_ok=True)
        with args.output.open("w", newline="") as f:
            writer = csv.DictWriter(f, fieldnames=results[0].keys())
            writer.writeheader()
            writer.writerows(results)
        logger.info("Results saved to %s", args.output)


if __name__ == "__main__":
    asyncio.run(analyze())
